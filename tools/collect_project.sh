#!/bin/bash

# Определяем текущую директорию как корень проекта
PROJECT_ROOT=$(pwd)

# ---------------------------------------------------------
# 1. Вывод tree -d
# ---------------------------------------------------------
echo "=========================================="
echo "1. PROJECT STRUCTURE (Directories Only)"
echo "=========================================="

if command -v tree &> /dev/null; then
    # Выводим только папки, исключая скрытые (например .git)
    tree -d -I '.git|vendor|node_modules|tools'
else
    # Если tree не установлен, используем find
    echo "[INFO] 'tree' command not found, using find instead."
    find . -type d -not -path '*/.*' | sed 's|[^/]*/| |g'
fi

echo "" # Пустая строка для разделения

# ---------------------------------------------------------
# 2. В цикле: путь и содержимое
# ---------------------------------------------------------
echo "=========================================="
echo "2. FILE CONTENTS"
echo "=========================================="

# Ищем файлы.
# Исключаем скрытые папки (.git, .idea) и бинарные файлы (по расширению).
# Добавьте нужные расширения в ! -name "*.test" и т.д., если нужно.
find . -type f \
    -not -path '*/\.*' \
    -not -path '*/vendor/*' \
    -not -name "*.mod" \
    -not -name "*.sum" \
    -not -name "*.exe" \
    -print0 | while IFS= read -r -d '' file; do

    # 2.1 Полный путь до файла
    # Используем realpath, чтобы получить абсолютный путь
    FULL_PATH=$(realpath "$file")
    
    # (Опционально) Проверка на бинарные файлы, чтобы не засорять вывод
    if file "$file" | grep -q "text"; then
        echo "--- FILE: $FULL_PATH ---"
        
        # 2.2 Содержимое файла
        cat "$file"
        
        echo "" # Добавляем пустую строку после каждого файла для читаемости
        echo "" 
    fi

done
